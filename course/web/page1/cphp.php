											<p>PHP code is interpreted by a web server with a PHP processor module, 
which generates the resulting web page: PHP commands can be embedded directly into an HTML source document rather 
than calling an external file to process data. It has also evolved to include a command line interface capability and can 
be used in standalone graphical applications.[8]
											</p>
											<p>PHP development began in 1994 when the developer Rasmus Lerdorf wrote a 
series of Common Gateway Interface (CGI) Perl scripts, which he used to maintain his personal homepage.
 The tools performed tasks such as displaying his resume and recording his web traffic.[5][10][11] He rewrote
 these scripts in C for performance reasons, extending them to add the ability to work with web forms and to
 communicate with databases, and called this implementation "Personal Home Page/Forms Interpreter" or PHP/FI.
											</p>
<p>PHP/FI could be used to build simple, dynamic web applications. Lerdorf initially announced the release of PHP/FI as
 "Personal Home Page Tools (PHP Tools) version 1.0" publicly to accelerate bug location and improve the code, on the comp.
infosystems.www.authoring.cgi Usenet discussion group on June 8, 1995.[12][13] This release already had the basic functionality 
that PHP has as of 2013. This included Perl-like variables, form handling, and the ability to embed HTML. The syntax
 resembled that of Perl but was simpler, more limited and less consistent.[5]
Early PHP was not intended to be a new programming language, and grew organically, with Lerdorf noting in retrospect:
 "I dont know how to stop it, there was never any intent to write a programming language I have absolutely no idea 
how to write a programming language, I just kept adding the next logical step on the way."[14] A development team began 
to form and, after months of work and beta testing, officially released PHP/FI 2 in November 1997.
One criticism of PHP is that it was not originally designed, but instead it was developed organically;[14] among other
 things, this has lead to inconsistent naming of functions and inconsistent ordering of their parameters.[15] In some
 cases, the function names were chosen to match the lower-level libraries which PHP was "wrapping",[16] while in some 
very early versions of PHP the length of the function names was used internally as a hash function, so names were chosen
 to improve the distribution of hash values.[17]
