											<p>
												The user interface of Android is based on direct manipulation, using touch inputs that 
loosely correspond to real-world actions, like swiping, tapping, pinching and reverse pinching to manipulate on screen objects.
 Internal hardware such as accelerometers, gyroscopes and proximity sensors are used by some applications to respond to 
additional user actions, for example adjusting the screen from portrait to landscape depending on how the device is oriented.
 Android allows users to customize their home screens with shortcuts to applications and widgets, which allow users to display
 live content, such as emails and weather information, directly on the home screen. Applications can further send notifications 
to the user to inform them of relevant information, such as new emails and text messages.
											</p>
											<p>
												Android's source code is released by Google under the Apache License;[12] this permissive 
licensing allows the software to be freely modified and distributed by device manufacturers, wireless carriers and
 enthusiast developers. Most Android devices ship with a combination of open source and proprietary software.[3] As
 of July 2013, Android has the largest number of applications ("apps"), available for download in Google Play store which
 has had over 1 million apps published, and over 50 billion downloads.[16] A developer survey conducted in April May
 2013 found that Android is the most used platform among developers: it is used by 71% of the mobile developers population.[17]
											</p>
<p>Android is popular with technology companies which require a ready-made, low-cost and customizable 
operating system for high-tech devices.[18] Despite being primarily designed for phones and tablets, it also 
