										<h3>Problem-based learning</h3>
									</header>
									<p>
										Problem-based learning (PBL) is a student-centered pedagogy
 in which students learn about a subject through the experience of problem solving. Students learn both thinking 
strategies and domain knowledge. The PBL format originated from the medical school of thought, and is now used in 
other schools of thought too. The goals of PBL are to help the students develop flexible knowledge, effective problem 
solving skills, self-directed learning, effective collaboration skills and intrinsic motivation. Problem-based learning is a style of active learning.
									</p>

									<header>
										<h3>Study skills</h3>
									</header>
									<p>
										Study skills or study strategies are approaches applied to learning.
 They are generally critical to success in school,[1] considered essential for acquiring good grades, and useful for 
learning throughout one's life.
There are an array of study skills, which may tackle the process of organizing and taking in new information,
 retaining information, or dealing with assessments. They include mnemonics, which aid the retention of lists of
 information, effective reading, and concentration techniques,[2] as well as efficient notetaking.[3][dead link]
While often left up to the student and their support network, study skills are increasingly taught in High School 
and at the University level. A number of books and websites are available, from works on specific techniques such 
as Tony Buzan's books on mind-mapping, to general guides to successful study such as those by Stella Cottrell.
									</p>
