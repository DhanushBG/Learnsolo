										<h3>Homework</h3>
									</header>
									<p>
										Homework, or homework assignment, refers to tasks assigned to students
 by their teachers to be completed outside the class. Common homework assignments may include a quantity 
or period of reading to be performed, writing or typing to be completed, problems to be solved, a school project 
to be built (such as a diorama or display), or other skills to be practiced.
									</p>
									<p>
										The basic objectives of assigning homework to students are 
the same as schooling in general: to increase the knowledge and improve the abilities and skills of the students.
 However, opponents of homework cite homework as rote, or grind work, designed to take up children's time, without 
offering tangible benefit.[2] Homework may be designed to reinforce what students have already learned,
 prepare them for upcoming (or complex or difficult) lessons, extend what they know by having them apply it 
to new situations, or to integrate their abilities by applying many different skills to a single task. Homework also
 provides an opportunity for parents to participate in their children's education.
									</p>
								</section>
