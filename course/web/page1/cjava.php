										<section>
											<h3>Java</h3>
											<p>
												Java is a computer programming language that is concurrent, 
class-based, object-oriented, and specifically designed to have as few implementation dependencies as possible. 
It is intended to let application developers write once, run anywhere (WORA), meaning that code that runs on 
one platform does not need to be recompiled to run on another. Java applications are typically compiled to bytecode
 (class file) that can run on any Java virtual machine (JVM) regardless of computer architecture. Java is, as of 2014,
 one of the most popular programming languages in use, particularly for client-server web applications, with a reported 
9 million developers.[10][11] Java was originally developed by James Gosling at Sun Microsystems (which has since merged
 into Oracle Corporation) and released in 1995 as a core component of Sun Microsystems' Java platform. The language
 derives much of its syntax from C and C++, but it has fewer low-level facilities than either of them.
											</p>
										</section>

										<section>
											
											<p>The original and reference implementation Java compilers,
 virtual machines, and class libraries were developed by Sun from 1991 and first released in 1995. As of May 2007, 
in compliance with the specifications of the Java Community Process, Sun relicensed most of its Java technologies under
 the GNU General Public License. Others have also developed alternative implementations of these Sun technologies, such
 as the GNU Compiler for Java (bytecode compiler), GNU Classpath (standard libraries), and IcedTea-Web (browser plugin for applets).
											</p>
											<p>James Gosling, Mike Sheridan, and Patrick Naughton initiated
 the Java language project in June 1991.[12] Java was originally designed for interactive television, but it was too
 advanced for the digital cable television industry at the time.[13] The language was initially called Oak after an oak 
tree that stood outside Gosling's office; it went by the name Green later, and was later renamed Java, from 
Java coffee,[14] said to be consumed in large quantities by the language's creators.[citation needed] Gosling aimed
 to implement a virtual machine and a language that had a familiar C/C++ style of notation.[15]
											</p>
<p>Sun Microsystems released the first public implementation as Java 1.0 in 1995.[1] It promised Write Once, Run Anywhere (WORA), 
providing no-cost run-times on popular platforms. Fairly secure and featuring configurable security, it allowed 
network- and file-access restrictions. Major web browsers soon incorporated the ability to run Java applets within web pages, 
and Java quickly became popular. With the advent of Java 2 (released initially as J2SE 1.2 in December 1998 to 1999), new versions
 had multiple configurations built for different types of platforms. For example, J2EE targeted enterprise applications and the greatly
 stripped-down version J2ME for mobile applications (Mobile Java). J2SE designated the Standard Edition. In 2006, for marketing
 purposes, Sun renamed new J2 versions as Java EE, Java ME, and Java SE, respectively.
											</p>
